public class Fudianshu {

    /**
     * 浮点数
     *
     * @param args
     */

    public static void main(String[] args) {

        float a = 0.9f;
        float b = 0.8f;
        float c = a - b;
        System.out.println(c);

        double d = 1f;
        double e = 0.7f;
        double f = d - e;
        System.out.println(f);

        System.out.println("1" + "2");
    }
}
